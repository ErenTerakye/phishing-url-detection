def markdown_from_sections(sections: list[dict]) -> str:
    lines = []
    for section in sections:
        stype = section["type"]
        if stype == "title":
            lines.append(f"# {section['text']}\n")
        elif stype == "heading":
            level = "#" * (section["level"] + 1)
            lines.append(f"{level} {section['text']}\n")
        elif stype == "paragraph":
            lines.append(section["text"] + "\n")
        elif stype == "table":
            lines.append(f"**{section['caption']}**\n")
            rows = section["rows"]
            header = rows[0]
            lines.append("| " + " | ".join(header) + " |")
            lines.append("| " + " | ".join(["---"] * len(header)) + " |")
            for row in rows[1:]:
                lines.append("| " + " | ".join(str(cell) for cell in row) + " |")
            lines.append("")
        elif stype == "figure":
            rel_path = Path("..") / section["path"].relative_to(ROOT)
            lines.append(f"![{section['caption']}]({rel_path})\n")
            lines.append(f"*{section['caption']}*\n")
    return "\n".join(lines).strip() + "\n"


def p_xml(text: str, style: str | None = None, bold: bool = False) -> str:
    style_xml = f'<w:pPr><w:pStyle w:val="{style}"/></w:pPr>' if style else ""
    bold_xml = "<w:b/>" if bold else ""
    return (
        "<w:p>"
        f"{style_xml}"
        "<w:r>"
        f"<w:rPr>{bold_xml}</w:rPr>"
        f"<w:t xml:space=\"preserve\">{escape(text)}</w:t>"
        "</w:r>"
        "</w:p>"
    )


def table_xml(rows: list[list[str]]) -> str:
    max_cols = max(len(r) for r in rows)
    grid = "".join('<w:gridCol w:w="2200"/>' for _ in range(max_cols))
    xml = [
        "<w:tbl>",
        '<w:tblPr><w:tblStyle w:val="TableGrid"/><w:tblW w:w="0" w:type="auto"/>'
        '<w:tblLook w:firstRow="1" w:lastRow="0" w:firstColumn="0" w:lastColumn="0" '
        'w:noHBand="0" w:noVBand="1"/></w:tblPr>',
        f"<w:tblGrid>{grid}</w:tblGrid>",
    ]
    for row_i, row in enumerate(rows):
        xml.append("<w:tr>")
        for cell in row:
            shade = '<w:shd w:fill="D9EAF7"/>' if row_i == 0 else ""
            xml.append(
                "<w:tc>"
                f'<w:tcPr><w:tcW w:w="2200" w:type="dxa"/>{shade}</w:tcPr>'
                f"{p_xml(str(cell), bold=(row_i == 0))}"
                "</w:tc>"
            )
        xml.append("</w:tr>")
    xml.append("</w:tbl>")
    return "".join(xml)


