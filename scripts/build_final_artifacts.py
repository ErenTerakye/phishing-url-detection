def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("--template", type=Path, default=DEFAULT_TEMPLATE)
    parser.add_argument("--docx-out", type=Path, default=REPORTS_DIR / "final_report_leakage_aware.docx")
    parser.add_argument("--md-out", type=Path, default=DOCS_DIR / "final_report.md")
    parser.add_argument("--presentation-out", type=Path, default=DOCS_DIR / "final_presentation_outline.md")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    REPORTS_DIR.mkdir(exist_ok=True)
    DOCS_DIR.mkdir(exist_ok=True)

    if not args.template.exists():
        raise FileNotFoundError(f"Template not found: {args.template}")

    metrics = read_metrics()
    sections = build_sections(metrics)
    args.md_out.write_text(markdown_from_sections(sections), encoding="utf-8")
    generate_docx(args.template, args.docx_out, sections)
    args.presentation_out.write_text(presentation_outline(metrics), encoding="utf-8")
    shutil.copy2(args.template, REPORTS_DIR / "ESOGUmmfSABLON_source.docx")

    print(f"Saved {args.md_out.relative_to(ROOT)}")
    print(f"Saved {args.docx_out.relative_to(ROOT)}")
    print(f"Saved {args.presentation_out.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
