def body_xml(sections: list[dict], sect_pr: str, image_map: dict[str, tuple[str, int, int, int]]) -> str:
    body_parts = []
    for section in sections:
        stype = section["type"]
        if stype == "title":
            body_parts.append(p_xml(section["text"], "Title", bold=True))
        elif stype == "heading":
            style = "Heading1" if section["level"] == 1 else "Heading2"
            body_parts.append(p_xml(section["text"], style, bold=True))
        elif stype == "paragraph":
            body_parts.append(p_xml(section["text"], "Normal"))
        elif stype == "table":
            body_parts.append(p_xml(section["caption"], "Normal", bold=True))
            body_parts.append(table_xml(section["rows"]))
            body_parts.append(p_xml(""))
        elif stype == "figure":
            rid, image_id, width_emu, height_emu = image_map[str(section["path"])]
            body_parts.append(image_xml(rid, image_id, width_emu, height_emu))
            body_parts.append(p_xml(section["caption"], "Normal", bold=True))
    return "".join(body_parts) + sect_pr


def generate_docx(template: Path, out_path: Path, sections: list[dict]) -> None:
    with TemporaryDirectory() as tmp:
        tmp_path = Path(tmp)
        with zipfile.ZipFile(template) as zin:
            zin.extractall(tmp_path)

        image_map: dict[str, tuple[str, int, int, int]] = {}
        rels_path = tmp_path / "word" / "_rels" / "document.xml.rels"
        rels_xml = rels_path.read_text(encoding="utf-8")
        for idx, section in enumerate([s for s in sections if s["type"] == "figure"], start=1):
            source = Path(section["path"])
            if not source.exists():
                continue
            media_name = f"project_figure_{idx}.png"
            shutil.copy2(source, tmp_path / "word" / "media" / media_name)
            rid = f"rIdProjectFigure{idx}"
            rel = (
                f'<Relationship Id="{rid}" '
                'Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/image" '
                f'Target="media/{media_name}"/>'
            )
            rels_xml = rels_xml.replace("</Relationships>", rel + "</Relationships>")
            with Image.open(source) as im:
                width_px, height_px = im.size
            width_emu = 5_900_000
            height_emu = int(width_emu * height_px / width_px)
            image_map[str(source)] = (rid, idx, width_emu, height_emu)
        rels_path.write_text(rels_xml, encoding="utf-8")

        doc_path = tmp_path / "word" / "document.xml"
        original = doc_path.read_text(encoding="utf-8")
        body_start = original.index("<w:body>") + len("<w:body>")
        body_end = original.index("</w:body>")
        body = original[body_start:body_end]
        sect_start = body.rfind("<w:sectPr")
        sect_pr = body[sect_start:] if sect_start != -1 else ""
        new_xml = original[:body_start] + body_xml(sections, sect_pr, image_map) + original[body_end:]
        doc_path.write_text(new_xml, encoding="utf-8")

        if out_path.exists():
            out_path.unlink()
        with zipfile.ZipFile(out_path, "w", zipfile.ZIP_DEFLATED) as zout:
            for path in tmp_path.rglob("*"):
                if path.is_file():
                    zout.write(path, path.relative_to(tmp_path))


