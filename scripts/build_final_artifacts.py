def fmt_pct(value) -> str:
    if pd.isna(value):
        return "-"
    return f"{float(value) * 100:.2f}%"


def fmt_num(value) -> str:
    if pd.isna(value):
        return "-"
    return f"{float(value):.4f}"


def top_row(df: pd.DataFrame) -> pd.Series:
    return df.sort_values(["F1-Score", "Recall"], ascending=False).iloc[0]


def metric_table(df: pd.DataFrame) -> list[list[str]]:
    cols = ["Model", "Accuracy", "Precision", "Recall", "F1-Score", "ROC-AUC", "Train Time (s)"]
    rows = [cols]
    for _, row in df[cols].iterrows():
        rows.append(
            [
                str(row["Model"]),
                fmt_pct(row["Accuracy"]),
                fmt_pct(row["Precision"]),
                fmt_pct(row["Recall"]),
                fmt_pct(row["F1-Score"]),
                fmt_num(row["ROC-AUC"]),
                f"{float(row['Train Time (s)']):.2f}",
            ]
        )
    return rows


